package java_basics;

public class Array {
	
public static void main(String[] args) {
		
		int value[] = new int[100];
		
		value[0] = 55;
		value[5] = 88;
		value[99] = 5854;
		
		System.out.println(value[5]);
	
		String word[] = new String[3];
		word[0] = "My";
		word[1] = "Name";
		word[2] = "Anish";
		
		System.out.println(word[0]);
		
		
  }
	
}
